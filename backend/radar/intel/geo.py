def detect_direction(text: str) -> str | None:
    """Return the direction key whose geo terms appear in `text` (case-insensitive,
    leading word-boundary so Russian case endings are caught), or None. First match
    wins (dict order) — see GEO_KEYWORDS ordering note."""
    if not text:
        return None
    low = text.lower()
    for key, terms in GEO_KEYWORDS.items():
        for t in terms:
            if re.search(r"(?<!\w)" + re.escape(t), low):
                return key
    return None
