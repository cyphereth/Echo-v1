def detect_place(text: str) -> tuple[str | None, str | None]:
    """Return (direction_key, city) for the first geo term found in `text`.

    Matching is case-insensitive with a leading word-boundary (so Russian case
    endings are caught, e.g. «в Судже» → Суджа). `city` is the canonical display name
    of the matched stem, or None for a region-level stem (oblast still returned).
    First match wins (dict order) — see GEO_KEYWORDS ordering note. No match → (None, None)."""
    if not text:
        return None, None
    low = text.lower()
    for key, terms in GEO_KEYWORDS.items():
        for stem, city in terms.items():
            if re.search(r"(?<!\w)" + re.escape(stem), low):
                return key, city
    return None, None


def detect_direction(text: str) -> str | None:
    """Return the direction key whose geo terms appear in `text`, or None.
    Thin wrapper over detect_place (kept for callers that only need the oblast)."""
    return detect_place(text)[0]
